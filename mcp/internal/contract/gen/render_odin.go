package main

import (
	"bytes"
	"fmt"
	"strings"
)

// odinConst maps a contract name (snake_case) to an Odin SCREAMING_SNAKE
// identifier suffix, e.g. "replay_behavior" -> "REPLAY_BEHAVIOR".
func odinConst(name string) string {
	return strings.ToUpper(name)
}

// renderOdin emits the generated Odin source for `package funpack`: field-name
// string consts for the version surface and the §28 envelope/command/event names.
// The funpack-team task lands this under funpack/ via the generator's -odin-out
// flag; this generator never writes into funpack/** itself.
//
// Note: the schema VALUES (ARTIFACT_SCHEMA_VERSION etc.) and the protocol-version
// VALUE remain hand-authored Odin constants pinned to the contract by the sync
// test — this file emits only the NAME surface, never re-declaring those values.
func renderOdin(c *ResolvedContract) ([]byte, error) {
	var b bytes.Buffer

	b.WriteString("// Code generated by mcp/internal/contract/gen; DO NOT EDIT.\n")
	b.WriteString("//\n")
	b.WriteString("// Source of truth: contract/funpack-api.json. Regenerate via the funpack-team\n")
	b.WriteString("// codegen task (`go run ./internal/contract/gen -odin-out <path>` from mcp/).\n")
	b.WriteString("// See ADR 2026-06-15-funpack-api-contract-dual-codegen.\n")
	b.WriteString("package funpack\n\n")

	b.WriteString(fmt.Sprintf("API_CONTRACT_VERSION :: %d\n\n", c.ContractVersion))

	// Version-surface field names.
	b.WriteString("// --- `funpack version --json` field names -----------------------------------\n\n")
	for _, f := range orderedFieldNames(c.VersionSurface) {
		b.WriteString(fmt.Sprintf("VERSION_FIELD_%s :: %q\n", odinConst(f), f))
	}
	b.WriteString("\n")
	b.WriteString("// --- schema names -----------------------------------------------------------\n\n")
	for _, name := range c.VersionSurface.SchemaNames {
		b.WriteString(fmt.Sprintf("SCHEMA_NAME_%s :: %q\n", odinConst(name), name))
	}
	b.WriteString("\n")

	// §28 protocol/transport name surface.
	b.WriteString("// --- §28 envelope field names -----------------------------------------------\n\n")
	for _, kind := range []string{"request", "response", "event"} {
		env, ok := c.Introspect.Envelopes[kind]
		if !ok {
			continue
		}
		for _, field := range env.Fields {
			b.WriteString(fmt.Sprintf("ENVELOPE_%s_FIELD_%s :: %q\n", odinConst(kind), odinConst(field), field))
		}
	}
	b.WriteString("\n")

	// §28 command names, grouped.
	b.WriteString("// --- §28 command names ------------------------------------------------------\n\n")
	for _, g := range sortedKeys(c.Introspect.CommandGroups) {
		grp := c.Introspect.CommandGroups[g]
		b.WriteString(fmt.Sprintf("// group %q (class %s)\n", g, grp.Class))
		for _, cmd := range commandNames(grp) {
			b.WriteString(fmt.Sprintf("CMD_%s :: %q\n", odinConst(cmd), cmd))
		}
		b.WriteString("\n")
	}

	// §28 event names.
	b.WriteString("// --- §28 event names --------------------------------------------------------\n\n")
	for _, e := range c.Introspect.Events {
		b.WriteString(fmt.Sprintf("EVENT_%s :: %q\n", odinConst(e), e))
	}

	// The MCP tools/list projection: one Tool_Spec per §28 command, schema +
	// dispatch hints from the SAME walk so the advertised input_schema cannot drift
	// from the dispatch arm.
	b.WriteString("\n")
	renderToolSpecs(&b, c)

	return b.Bytes(), nil
}

// odinString writes s as a double-quoted Odin string literal, escaping the few
// characters an Odin string literal cannot carry raw. Contract docs are plain
// ASCII prose, so only the quote and backslash need escaping; '\n' is escaped
// defensively for byte-stable single-line literals.
func odinString(s string) string {
	r := strings.NewReplacer(`\`, `\\`, `"`, `\"`, "\n", `\n`, "\t", `\t`)
	return `"` + r.Replace(s) + `"`
}

// renderToolSpecs emits the generated Tool_Spec table: one entry per §28 command,
// projecting the contract's per-command arg shapes into an MCP tools/list
// input_schema (a session_id-keyed superset, plus an optional `branch` selector for
// every observe-class command) AND the dispatch hints (group, class, wire command).
// One source — the contract walk — so the schema a future tools/list advertises and
// the arm tools/call dispatches into cannot drift. Groups iterate in sorted order,
// commands in on-disk declaration order, args in sorted-key order — byte-stable.
func renderToolSpecs(b *bytes.Buffer, c *ResolvedContract) {
	b.WriteString("// --- MCP tools/list projection (Tool_Spec table) ----------------------------\n")
	b.WriteString("//\n")
	b.WriteString("// Generated FROM contract/funpack-api.json: each §28 command_groups command AND\n")
	b.WriteString("// each server_tools tool becomes one MCP tool in the SAME table. A §28 command's\n")
	b.WriteString("// input_schema is the wire `args` shape PLUS the always-present session_id and\n")
	b.WriteString("// (for observe-class commands) the optional `branch` selector; a server-native\n")
	b.WriteString("// tool's input_schema is its `args` verbatim. tools/list reads TOOL_SPECS;\n")
	b.WriteString("// tools/call dispatches on .command / .group — one source so the advertised\n")
	b.WriteString("// schema cannot drift from dispatch.\n\n")

	b.WriteString("// Tool_Arg is one MCP input_schema property: the JSON name, its JSON-Schema\n")
	b.WriteString("// type, whether it is required, and its description.\n")
	b.WriteString("Tool_Arg :: struct {\n")
	b.WriteString("\tname:      string,\n")
	b.WriteString("\tjson_type: string,\n")
	b.WriteString("\trequired:  bool,\n")
	b.WriteString("\tdoc:       string,\n")
	b.WriteString("}\n\n")

	b.WriteString("// Tool_Spec is one MCP tool: its advertised tool name and full input_schema\n")
	b.WriteString("// (args), plus the dispatch hints tools/call switches on (the wire command, its\n")
	b.WriteString("// declaring group/family, and the determinism class). It carries BOTH classes of\n")
	b.WriteString("// MCP tool from one table: a §28 session command (session_scoped = true, group is\n")
	b.WriteString("// its §28 group, command is the wire command) and a server-native tool\n")
	b.WriteString("// (session_scoped = false, group is its dispatch family, command equals the tool\n")
	b.WriteString("// name — no §28 wire command exists). One unified table is the whole point: every\n")
	b.WriteString("// tool tools/list advertises and tools/call dispatches reads from here.\n")
	b.WriteString("Tool_Spec :: struct {\n")
	b.WriteString("\tname:           string,\n")
	b.WriteString("\tcommand:        string,\n")
	b.WriteString("\tgroup:          string,\n")
	b.WriteString("\tclass:          string,\n")
	b.WriteString("\tsession_scoped: bool,\n")
	b.WriteString("\targs:           []Tool_Arg,\n")
	b.WriteString("}\n\n")

	b.WriteString("// SESSION_ID_ARG is the universal session selector every §28 tool carries — the\n")
	b.WriteString("// MCP handle that keys the live debug session a tools/call dispatches into.\n")
	b.WriteString("SESSION_ID_ARG :: Tool_Arg{\n")
	b.WriteString("\tname      = \"session_id\",\n")
	b.WriteString("\tjson_type = \"string\",\n")
	b.WriteString("\trequired  = true,\n")
	b.WriteString("\tdoc       = \"opaque id of the live session, as returned by session_start\",\n")
	b.WriteString("}\n\n")

	b.WriteString("// BRANCH_ARG is the optional §28 observe-addressing selector every observe-class\n")
	b.WriteString("// tool carries: omitted reads the canonical timeline, set reads a checkout-\n")
	b.WriteString("// created branch.\n")
	b.WriteString("BRANCH_ARG :: Tool_Arg{\n")
	b.WriteString("\tname      = \"branch\",\n")
	b.WriteString("\tjson_type = \"string\",\n")
	b.WriteString("\trequired  = false,\n")
	b.WriteString("\tdoc       = \"optional §28 observe-addressing selector: omit for the canonical timeline, set to a checkout-created branch name\",\n")
	b.WriteString("}\n\n")

	b.WriteString("// TOOL_SPECS is the generated tools/list projection: the FULL MCP tool surface\n")
	b.WriteString("// in one table — the §28 commands first (groups sorted, commands as declared),\n")
	b.WriteString("// then the server-native tools (families sorted, tools as declared). Both\n")
	b.WriteString("// sections are projected from contract/funpack-api.json by the same walk, so the\n")
	b.WriteString("// advertised input_schema cannot drift from dispatch. A package-level `:=` (not\n")
	b.WriteString("// `::`) because an Odin slice literal is not a compile-time constant; it is\n")
	b.WriteString("// read-only by convention (a generated table tools/list walks, never mutates).\n")
	b.WriteString("TOOL_SPECS := []Tool_Spec{\n")
	for _, g := range sortedKeys(c.Introspect.CommandGroups) {
		grp := c.Introspect.CommandGroups[g]
		for _, cmd := range grp.Commands {
			b.WriteString(fmt.Sprintf("\t{\n\t\tname           = %q,\n", grp.toolName(cmd.Name)))
			b.WriteString(fmt.Sprintf("\t\tcommand        = %q,\n", cmd.Name))
			b.WriteString(fmt.Sprintf("\t\tgroup          = %q,\n", g))
			b.WriteString(fmt.Sprintf("\t\tclass          = %q,\n", grp.Class))
			b.WriteString("\t\tsession_scoped = true,\n")
			b.WriteString("\t\targs           = []Tool_Arg{\n")
			b.WriteString("\t\t\tSESSION_ID_ARG,\n")
			for _, argName := range sortedKeys(cmd.Args) {
				arg := cmd.Args[argName]
				b.WriteString("\t\t\t{")
				b.WriteString(fmt.Sprintf("name = %q, ", argName))
				b.WriteString(fmt.Sprintf("json_type = %q, ", arg.Type))
				b.WriteString(fmt.Sprintf("required = %t, ", arg.Required))
				b.WriteString(fmt.Sprintf("doc = %s", odinString(arg.Doc)))
				b.WriteString("},\n")
			}
			if grp.Class == "observe" {
				b.WriteString("\t\t\tBRANCH_ARG,\n")
			}
			b.WriteString("\t\t},\n")
			b.WriteString("\t},\n")
		}
	}
	renderServerToolSpecs(b, c)
	b.WriteString("}\n")
}

// renderServerToolSpecs appends the server-native tools to the SAME TOOL_SPECS
// literal the §28 commands feed. Each server-native tool projects into one
// Tool_Spec whose group is its FAMILY (the downstream tools/call arm key), class
// is the family's determinism class, session_scoped is the family's flag (false for
// every server-native tool), command IS the tool name (no §28 wire command exists),
// and args are the tool's full input_schema verbatim — NO injected SESSION_ID_ARG or
// BRANCH_ARG (those are §28-specific). Families iterate in sorted order, tools in
// on-disk declaration order, args in sorted-key order — byte-stable.
func renderServerToolSpecs(b *bytes.Buffer, c *ResolvedContract) {
	for _, famName := range sortedKeys(c.ServerTools.Families) {
		fam := c.ServerTools.Families[famName]
		for _, tool := range fam.Tools {
			b.WriteString(fmt.Sprintf("\t{\n\t\tname           = %q,\n", tool.Name))
			b.WriteString(fmt.Sprintf("\t\tcommand        = %q,\n", tool.Name))
			b.WriteString(fmt.Sprintf("\t\tgroup          = %q,\n", famName))
			b.WriteString(fmt.Sprintf("\t\tclass          = %q,\n", fam.Class))
			b.WriteString(fmt.Sprintf("\t\tsession_scoped = %t,\n", fam.SessionScoped))
			b.WriteString("\t\targs           = []Tool_Arg{\n")
			for _, argName := range sortedKeys(tool.Args) {
				arg := tool.Args[argName]
				b.WriteString("\t\t\t{")
				b.WriteString(fmt.Sprintf("name = %q, ", argName))
				b.WriteString(fmt.Sprintf("json_type = %q, ", arg.Type))
				b.WriteString(fmt.Sprintf("required = %t, ", arg.Required))
				b.WriteString(fmt.Sprintf("doc = %s", odinString(arg.Doc)))
				b.WriteString("},\n")
			}
			b.WriteString("\t\t},\n")
			b.WriteString("\t},\n")
		}
	}
}

// orderedFieldNames returns version_surface.fields keys in deterministic order.
func orderedFieldNames(vs ResolvedVersionSurface) []string {
	return sortedKeys(vs.Fields)
}
