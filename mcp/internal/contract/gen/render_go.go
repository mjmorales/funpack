package main

import (
	"bytes"
	"fmt"
	"go/format"
	"strings"
)

// goConst maps a contract command/event/schema name (snake_case) to its Go
// identifier suffix in PascalCase, e.g. "replay_behavior" -> "ReplayBehavior".
func goConst(name string) string {
	parts := strings.Split(name, "_")
	for i, p := range parts {
		if p == "" {
			continue
		}
		parts[i] = strings.ToUpper(p[:1]) + p[1:]
	}
	return strings.Join(parts, "")
}

// renderGo emits the generated Go source for package contract, gofmt-canonical
// and byte-stable across runs.
func renderGo(c *ResolvedContract) ([]byte, error) {
	var b bytes.Buffer

	b.WriteString("// Code generated by internal/contract/gen; DO NOT EDIT.\n")
	b.WriteString("//\n")
	b.WriteString("// Source of truth: contract/funpack-api.json. Regenerate with\n")
	b.WriteString("// `go run ./internal/contract/gen` from the mcp module. See ADR\n")
	b.WriteString("// 2026-06-15-funpack-api-contract-dual-codegen.\n")
	b.WriteString("package contract\n\n")

	b.WriteString("import \"encoding/json\"\n\n")

	b.WriteString("// ContractVersion is the funpack-api.json schema revision this code was generated from.\n")
	b.WriteString(fmt.Sprintf("const ContractVersion = %d\n\n", c.ContractVersion))

	renderVersionSurface(&b, c)
	renderIntrospectMeta(&b, c)
	renderEnvelopes(&b, c)
	renderCommands(&b, c)
	renderEvents(&b, c)

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		// Return the unformatted bytes alongside the error so a generator bug is
		// debuggable from the raw output.
		return b.Bytes(), fmt.Errorf("gofmt generated source: %w", err)
	}
	return formatted, nil
}

func renderVersionSurface(b *bytes.Buffer, c *ResolvedContract) {
	vs := c.VersionSurface

	b.WriteString("// VersionInfo is the decoded shape of `funpack version --json`. An absent schema\n")
	b.WriteString("// key in Schemas means the build does not expose that schema yet (not an error).\n")
	b.WriteString("type VersionInfo struct {\n")
	b.WriteString("\tVersion string         `json:\"version\"`\n")
	b.WriteString("\tSchemas map[string]int `json:\"schemas\"`\n")
	b.WriteString("}\n\n")

	b.WriteString("// VersionArgv is the argv that produces VersionInfo JSON on stdout.\n")
	b.WriteString("var VersionArgv = []string{")
	for i, a := range vs.Argv {
		if i > 0 {
			b.WriteString(", ")
		}
		b.WriteString(fmt.Sprintf("%q", a))
	}
	b.WriteString("}\n\n")

	b.WriteString("// Schema-name constants — the keys VersionInfo.Schemas may carry.\n")
	b.WriteString("const (\n")
	for _, name := range vs.SchemaNames {
		b.WriteString(fmt.Sprintf("\tSchema%s = %q\n", goConst(name), name))
	}
	b.WriteString(")\n\n")

	b.WriteString("// VersionRange is the inclusive [Min, Max] schema-version window this MCP build accepts.\n")
	b.WriteString("type VersionRange struct {\n")
	b.WriteString("\tMin int\n")
	b.WriteString("\tMax int\n")
	b.WriteString("}\n\n")

	b.WriteString("// Contains reports whether v falls within the inclusive range.\n")
	b.WriteString("func (r VersionRange) Contains(v int) bool { return v >= r.Min && v <= r.Max }\n\n")

	b.WriteString("// Supported is the MCP compat policy: per-schema the funpack versions this build\n")
	b.WriteString("// accepts. Preflight refuses a PRESENT schema outside its range; an ABSENT schema\n")
	b.WriteString("// never refuses (§29 Index-Contract discipline).\n")
	b.WriteString("var Supported = map[string]VersionRange{\n")
	for _, name := range sortedKeys(vs.Supported) {
		r := vs.Supported[name]
		b.WriteString(fmt.Sprintf("\tSchema%s: {Min: %d, Max: %d},\n", goConst(name), r.Min, r.Max))
	}
	b.WriteString("}\n\n")
}

func renderIntrospectMeta(b *bytes.Buffer, c *ResolvedContract) {
	in := c.Introspect
	b.WriteString("// ProtocolVersion is the §28 exact-match envelope version every message stamps as `v`.\n")
	b.WriteString(fmt.Sprintf("const ProtocolVersion = %d\n\n", in.ProtocolVersion))
	b.WriteString("// Transport names the §28 wire transport.\n")
	b.WriteString(fmt.Sprintf("const Transport = %q\n\n", in.Transport))
}

func renderEnvelopes(b *bytes.Buffer, c *ResolvedContract) {
	in := c.Introspect

	b.WriteString("// --- §28 envelopes -----------------------------------------------------------\n")
	b.WriteString("//\n")
	b.WriteString("// Three closed message kinds, each a fixed-order field tuple, versioned exact-match.\n")
	b.WriteString("// Every envelope stamps `v` == ProtocolVersion; a consumer refuses a version or\n")
	b.WriteString("// field-shape mismatch rather than best-effort parsing.\n\n")

	if req, ok := in.Envelopes["request"]; ok {
		writeDocComment(b, "Request", req.Doc)
		b.WriteString("type Request struct {\n")
		b.WriteString("\tV    int             `json:\"v\"`\n")
		b.WriteString("\tID   int64           `json:\"id\"`\n")
		b.WriteString("\tCmd  string          `json:\"cmd\"`\n")
		b.WriteString("\tArgs json.RawMessage `json:\"args,omitempty\"`\n")
		b.WriteString("}\n\n")
	}

	if resp, ok := in.Envelopes["response"]; ok {
		writeDocComment(b, "Response", resp.Doc)
		b.WriteString("//\n")
		b.WriteString("// Exactly-one invariant: when Ok is true, Result is non-nil and Error is nil; when\n")
		b.WriteString("// Ok is false, Error is non-nil and Result is nil — never both, never neither.\n")
		b.WriteString("type Response struct {\n")
		b.WriteString("\tV      int              `json:\"v\"`\n")
		b.WriteString("\tID     int64            `json:\"id\"`\n")
		b.WriteString("\tOk     bool             `json:\"ok\"`\n")
		b.WriteString("\tCmd    string           `json:\"cmd\"`\n")
		b.WriteString("\tResult *json.RawMessage `json:\"result,omitempty\"`\n")
		b.WriteString("\tError  *string          `json:\"error,omitempty\"`\n")
		b.WriteString("}\n\n")
	}

	if ev, ok := in.Envelopes["event"]; ok {
		writeDocComment(b, "Event", ev.Doc)
		b.WriteString("//\n")
		b.WriteString("// The payload is open: an unsolicited push correlated by Name (not an id).\n")
		b.WriteString("// Payload captures every field beyond `v`/`event` (e.g. breakpoint_hit/watch_fired\n")
		b.WriteString("// carry target and value).\n")
		b.WriteString("type Event struct {\n")
		b.WriteString("\tV       int                        `json:\"v\"`\n")
		b.WriteString("\tName    string                     `json:\"event\"`\n")
		b.WriteString("\tPayload map[string]json.RawMessage `json:\"-\"`\n")
		b.WriteString("}\n\n")
	}
}

func renderCommands(b *bytes.Buffer, c *ResolvedContract) {
	in := c.Introspect

	b.WriteString("// --- §28 command taxonomy ----------------------------------------------------\n\n")

	b.WriteString("// Class is a command's determinism class. observe = non-perturbing (reads the\n")
	b.WriteString("// canonical chain by default, accepts an optional `branch` arg); control =\n")
	b.WriteString("// perturbing -> forks a non-warranted branch off a snapshot.\n")
	b.WriteString("type Class string\n\n")
	b.WriteString("const (\n")
	b.WriteString("\tClassObserve Class = \"observe\"\n")
	b.WriteString("\tClassControl Class = \"control\"\n")
	b.WriteString(")\n\n")

	b.WriteString("// Command is a §28 command name.\n")
	b.WriteString("type Command string\n\n")

	// Command-name consts, grouped by their contract group, in deterministic order.
	b.WriteString("const (\n")
	groups := sortedKeys(in.CommandGroups)
	for gi, g := range groups {
		grp := in.CommandGroups[g]
		if gi > 0 {
			b.WriteString("\n")
		}
		b.WriteString(fmt.Sprintf("\t// group %q (class %s)\n", g, grp.Class))
		for _, cmd := range grp.Commands {
			b.WriteString(fmt.Sprintf("\tCmd%s Command = %q\n", goConst(cmd), cmd))
		}
	}
	b.WriteString(")\n\n")

	// Class lookup.
	b.WriteString("// commandClass maps each Command to the determinism Class of its group.\n")
	b.WriteString("var commandClass = map[Command]Class{\n")
	for _, g := range groups {
		grp := in.CommandGroups[g]
		class := "ClassObserve"
		if grp.Class == "control" {
			class = "ClassControl"
		}
		for _, cmd := range grp.Commands {
			b.WriteString(fmt.Sprintf("\tCmd%s: %s,\n", goConst(cmd), class))
		}
	}
	b.WriteString("}\n\n")

	// Group lookup.
	b.WriteString("// commandGroup maps each Command to the name of the group that declares it.\n")
	b.WriteString("var commandGroup = map[Command]string{\n")
	for _, g := range groups {
		grp := in.CommandGroups[g]
		for _, cmd := range grp.Commands {
			b.WriteString(fmt.Sprintf("\tCmd%s: %q,\n", goConst(cmd), g))
		}
	}
	b.WriteString("}\n\n")

	// ordered command list.
	b.WriteString("// Commands is every §28 command in contract order (groups sorted, commands as declared).\n")
	b.WriteString("var Commands = []Command{\n")
	for _, g := range groups {
		grp := in.CommandGroups[g]
		for _, cmd := range grp.Commands {
			b.WriteString(fmt.Sprintf("\tCmd%s,\n", goConst(cmd)))
		}
	}
	b.WriteString("}\n\n")

	// Accessors.
	b.WriteString("// ClassOf returns the determinism class of cmd and whether cmd is a known command.\n")
	b.WriteString("func ClassOf(cmd Command) (Class, bool) {\n")
	b.WriteString("\tclass, ok := commandClass[cmd]\n")
	b.WriteString("\treturn class, ok\n")
	b.WriteString("}\n\n")

	b.WriteString("// GroupOf returns the declaring group of cmd and whether cmd is a known command.\n")
	b.WriteString("func GroupOf(cmd Command) (string, bool) {\n")
	b.WriteString("\tgroup, ok := commandGroup[cmd]\n")
	b.WriteString("\treturn group, ok\n")
	b.WriteString("}\n\n")

	b.WriteString("// IsControl reports whether cmd is a perturbing control command.\n")
	b.WriteString("func IsControl(cmd Command) bool { return commandClass[cmd] == ClassControl }\n\n")
}

func renderEvents(b *bytes.Buffer, c *ResolvedContract) {
	in := c.Introspect

	b.WriteString("// EventName is a §28 async event name.\n")
	b.WriteString("type EventName string\n\n")
	b.WriteString("const (\n")
	for _, e := range in.Events {
		b.WriteString(fmt.Sprintf("\tEvent%s EventName = %q\n", goConst(e), e))
	}
	b.WriteString(")\n\n")

	b.WriteString("// Events is every §28 event name in contract order.\n")
	b.WriteString("var Events = []EventName{\n")
	for _, e := range in.Events {
		b.WriteString(fmt.Sprintf("\tEvent%s,\n", goConst(e)))
	}
	b.WriteString("}\n")
}

// writeDocComment writes a Go doc comment for typeName seeded with the contract's
// doc string, wrapped onto comment lines.
func writeDocComment(b *bytes.Buffer, typeName, doc string) {
	b.WriteString(fmt.Sprintf("// %s is the §28 %s envelope.\n", typeName, strings.ToLower(typeName)))
	if doc == "" {
		return
	}
	b.WriteString("//\n")
	for _, line := range wrapComment(doc, 76) {
		b.WriteString("// ")
		b.WriteString(line)
		b.WriteString("\n")
	}
}

// wrapComment greedily wraps s onto lines no longer than width runes.
func wrapComment(s string, width int) []string {
	words := strings.Fields(s)
	if len(words) == 0 {
		return nil
	}
	var lines []string
	cur := words[0]
	for _, w := range words[1:] {
		if len(cur)+1+len(w) > width {
			lines = append(lines, cur)
			cur = w
			continue
		}
		cur += " " + w
	}
	lines = append(lines, cur)
	return lines
}
